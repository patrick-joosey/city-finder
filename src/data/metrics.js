// ============================================================
// SCORING FORMULAS — coefficient conventions
// ============================================================
// Every formula is a weighted sum of raw metrics, clamped to [1, 10].
//
// Coefficients were chosen empirically with these principles:
//   * Multipliers sized so realistic max inputs total ~10-14 pre-clamp.
//     This produces spread (cities differentiate) without saturating too often.
//   * Log-scaled inputs (log2 for counts in tens-to-hundreds, log10 for
//     counts in thousands+) model diminishing returns — the jump from 1→3
//     meetings matters more than 20→22.
//   * Linear inputs for 1-3 ordinal scores (e.g., hospitalScore).
//   * Caps on inflated inputs (e.g., therapistsPer10k capped at 30 because
//     Psychology Today counts include teletherapy-expanded results that
//     inflate small markets).
//
// When adjusting a coefficient, pair it with a note explaining the trade-off
// and re-verify the rank order for the sanity-check cities: Boulder, DC,
