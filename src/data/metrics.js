export function computeScores(cityId) {
  const metrics = rawMetrics[cityId];
  if (!metrics) return null;
  const scores = {};
  for (const [key, formula] of Object.entries(formulas)) {
    if (metrics[key]) {
      scores[key] = Math.round(formula.compute(metrics[key]) * 10) / 10;
    } else {
      scores[key] = 5; // Default for missing data
    }
  }
  return scores;
}

export function computeAllScores() {
  const all = {};
  for (const id of Object.keys(rawMetrics)) {
    all[id] = computeScores(parseInt(id));
  }
  return all;
}
